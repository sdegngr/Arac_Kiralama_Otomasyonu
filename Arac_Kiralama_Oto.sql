  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "SYSTEM"."GIRIS_PROCEDURE" 
(
  IN_NICK IN VARCHAR2 
, IN_SIFRE IN VARCHAR2 
, OUT_SONUC OUT VARCHAR2
, OUT_YETKI OUT VARCHAR2
, OUT_AD OUT VARCHAR2
, OUT_SOYAD OUT VARCHAR2
) AS 

TEMP_NICK VARCHAR(100);
TEMP_SIFRE VARCHAR(100);

BEGIN
  
  
  SELECT NICK INTO TEMP_NICK FROM KULLANICI WHERE (NICK = IN_NICK) and (SIFRE = IN_SIFRE);
  SELECT SIFRE INTO TEMP_SIFRE FROM KULLANICI WHERE (NICK = IN_NICK) and (SIFRE = IN_SIFRE);
  
  
  
  IF IN_NICK = TEMP_NICK and IN_SIFRE = TEMP_SIFRE THEN
    OUT_SONUC := 'GIRIS_BASARILI';
    
  ELSIF  IN_NICK = TEMP_NICK and IN_SIFRE != TEMP_SIFRE THEN
    OUT_SONUC := 'SIFRE_YANLIS';
  ELSIF  IN_NICK != TEMP_NICK and IN_SIFRE = TEMP_SIFRE THEN
    OUT_SONUC := 'NICK_YANLIS';
  ELSE
    OUT_SONUC := 'IKISI_DE_YANLIS';
  
  END IF;
  
  
  IF (OUT_SONUC = 'GIRIS_BASARILI') THEN
    SELECT YETKI INTO OUT_YETKI FROM KULLANICI WHERE (NICK = IN_NICK) and (SIFRE = IN_SIFRE);
    SELECT ADI INTO OUT_AD FROM KULLANICI WHERE (NICK = IN_NICK) and (SIFRE = IN_SIFRE);
    SELECT SOYADI INTO OUT_SOYAD FROM KULLANICI WHERE (NICK = IN_NICK) and (SIFRE = IN_SIFRE);
    
  ELSE 
    NULL;
    
  END IF;
  
END GIRIS_PROCEDURE;

