  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "SYSTEM"."ARACKIRALAMA" 
(
  IN_PLAKA_BILGISI IN VARCHAR,
  IN_MARKA IN VARCHAR,
  IN_TIP IN VARCHAR,
  IN_SON_KM IN int,
  IN_VITES IN VARCHAR,
  IN_MUSTERI_TC IN VARCHAR,
  IN_MUSTERI_ADI IN VARCHAR,
  IN_MUSTERI_SOYADI IN VARCHAR,
  IN_BAS_TAR IN DATE,
  IN_BAS_SA IN VARCHAR,
  IN_BIT_TAR IN DATE,
  IN_BIT_SA IN VARCHAR,
  IN_ARAC_FIYATI int,
  IN_KUR IN VARCHAR
) AS 
    
    TEL_NO2 VARCHAR(11);
    TEMP_GUN_FARKI NUMBER;
    TEMP_TOPLAM_PARA NUMBER;
    TEMP_BORC NUMBER;
    TEMP_TOTAL NUMBER;
    
BEGIN
    
    SELECT TEL_NO INTO TEL_NO2 FROM MUSTERI_BILGILERI WHERE MUSTERI_TC = IN_MUSTERI_TC;
        
    
    INSERT INTO KIRALANAN_ARAC_BILGILERI VALUES (IN_PLAKA_BILGISI, IN_MARKA, IN_TIP,IN_VITES,IN_SON_KM, IN_ARAC_FIYATI,IN_KUR, 
    IN_MUSTERI_TC, IN_MUSTERI_ADI, IN_MUSTERI_SOYADI, TEL_NO2, IN_BAS_TAR, IN_BAS_SA, IN_BIT_TAR, IN_BIT_SA );
    
    TEMP_GUN_FARKI := TRUNC(IN_BIT_TAR) - TRUNC(IN_BAS_TAR);
    TEMP_TOPLAM_PARA := IN_ARAC_FIYATI * TEMP_GUN_FARKI;
    
    UPDATE MUSTERI_BILGILERI SET BAKIYE = BAKIYE+TEMP_TOPLAM_PARA WHERE MUSTERI_TC = IN_MUSTERI_TC;
    SELECT BAKIYE INTO TEMP_TOTAL FROM MUSTERI_BILGILERI WHERE MUSTERI_TC = IN_MUSTERI_TC;
    

    INSERT INTO MUSTERI_HESAP_EKS (PLAKA,ALACAK,BORC,TOTAL,MUSTERI_TC,MUSTERI_ADI,MUSTERI_SOYADI,ACIKLAMA,BAS_TARIHI,TUR)
    VALUES (IN_PLAKA_BILGISI,0, TEMP_TOPLAM_PARA, TEMP_TOPLAM_PARA, IN_MUSTERI_TC, IN_MUSTERI_ADI, IN_MUSTERI_SOYADI, 'FATURA KESÝLMEDÝ', IN_BIT_TAR, 'KÝRALAMA');
    
   
    
    
    
END ARACKIRALAMA;

