  CREATE OR REPLACE NONEDITIONABLE PROCEDURE "SYSTEM"."KONTRAT_KALDIR" 
(
  IN_PLAKA IN VARCHAR2 
, IN_MUSTERI_TC IN VARCHAR2
, IN_DONUS_KM IN NUMBER
, IN_KDV IN NUMBER
, IN_TESLIM_EDEN IN VARCHAR2
, IN_DONUS_TARIHI IN DATE
, IN_KDON_TIME IN VARCHAR2

) AS 
 
TEMP_BAS_TAR DATE;
TEMP_GUN_FARKI NUMBER;
TEMP_ARAC_FIYATI NUMBER;
TEMP_TELNO VARCHAR2(11);
TEMP_TOPLAM_PARA NUMBER;

BEGIN

    SELECT BAS_TARIHI INTO TEMP_BAS_TAR FROM KIRALANAN_ARAC_BILGILERI WHERE PLAKA = IN_PLAKA;
    
    
    TEMP_GUN_FARKI := TRUNC(IN_DONUS_TARIHI) - TRUNC(TEMP_BAS_TAR);
    
    
    SELECT ARAC_FIYATI INTO TEMP_ARAC_FIYATI FROM KIRALANAN_ARAC_BILGILERI WHERE PLAKA = IN_PLAKA;
    
    TEMP_TOPLAM_PARA := TEMP_ARAC_FIYATI * TEMP_GUN_FARKI;
    
    
    
    SELECT TEL_NO INTO TEMP_TELNO FROM MUSTERI_BILGILERI WHERE MUSTERI_TC = IN_MUSTERI_TC;
    
    
    
    INSERT INTO KAPANAN_SOZLESMELER (PLAKA,MARKA,TIP,VITES,BAS_KM,ARAC_FIYATI,KUR,MUSTERI_TC,MUSTERI_ADI,MUSTERI_SOYADI,BAS_TARIHI,BAS_SAATI)
    SELECT PLAKA,MARKA,TIP,VITES,SON_KM,ARAC_FIYATI,KUR,MUSTERI_TC,MUSTERI_ADI,MUSTERI_SOYADI,BAS_TARIHI,BAS_SAATI 
    FROM KIRALANAN_ARAC_BILGILERI WHERE PLAKA=IN_PLAKA;
    
    UPDATE KAPANAN_SOZLESMELER SET 
    SON_KM = IN_DONUS_KM,
    ODENEN_TOPLAM_MIKTAR = TEMP_TOPLAM_PARA,
    KDV_BEDELÝ = IN_KDV,
    KIRALANAN_GUN_SAYISI = TEMP_GUN_FARKI,
    TESLIM_EDEN = IN_TESLIM_EDEN,
    TEL_NO = TEMP_TELNO,
    BITIS_TARIHI = IN_DONUS_TARIHI,
    BITIS_SAATI = IN_KDON_TIME
    WHERE PLAKA = IN_PLAKA;
    
    
    
    
    DELETE FROM KIRALANAN_ARAC_BILGILERI WHERE PLAKA = IN_PLAKA;
    
    UPDATE BUTUN_ARAC_BILGILERI
    SET SON_KM = IN_DONUS_KM
    WHERE PLAKA = IN_PLAKA;
    
    
    
    
    
END KONTRAT_KALDIR;

